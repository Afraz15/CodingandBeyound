                                        # LISTS

spam = ['cats', 'dogs', 'fish', 'ducks']
num = [1, 2, 3, 4, 5]

add = spam + num                        # this is how we add two list variables...
print(add)

multi = spam * 2                        # when we multiply a number with a list varibale it will result in multiplying the list values like cat * 2 will be cat, cat
print(multi)        

multiply = num * 5                      # same goes to the numbers in it , they won't be multiplied like this 2 * 5 = 10 but 2 * 5 = 2 2 2 2 2
print(multiply)







