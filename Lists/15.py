                                    # FINDING VALUE IN LIST

spam = ['pubg', 'fortnite', 'freefire', 'css']
print(spam.index('css'))            # the .index('name of the value') will help us find the number that value hold in the list variable...
print(spam.index('freefire'))

Spam = ['pubg', 'fortnite', 'freefire', 'css', 'fortnite']
print(Spam.index('fortnite'))       # if there are more than one same values then it will print out the number of the value which comes first...

print(Spam.append('csgo'))          # we can add a value in the list like this
print(Spam)

print(Spam.insert(1,'dota'))
