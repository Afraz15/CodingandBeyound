                                        # INSERT/REMOVE VALUE IN LIST

spam = ['pubg', 'fortnite', 'freefire', 'css']
spam.append('dota')                     # we just added a value in list and this value will be in the last
print(spam)

spam.insert(1,'mmo')                    # we can add a value on a specific position just like this as well..
print(spam)

Spam = ['pubg', 'fortnite', 'freefire', 'css']
Spam.remove('css')                      # we can remove the value in the list like this...  
print(Spam)
# these are list commands and they can only be called in list variable...   











