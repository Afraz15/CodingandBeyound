                                    # USING ESCAPE CHARACTER

spam1 = "hello \'' there"
spam2 = "hello \' there"
spam3 = "hello \t there"
spam4 = "hello \n there"
spam5 = "hello \\ there"
spam = [spam1,spam2,spam3,spam4,spam5]
i = 1

while i < 5:
    print(spam[i])                 # using this mathod we can summon all the values one by one
    i = i + 1
    















