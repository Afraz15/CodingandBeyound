                                    # USING IN AND NOT IN OPERATOR

h = 'hello world'

if 'hello' in h:                    # this is how we use ' in ' operator
    print(True)
else:
    print(False)

if 'good' in h:
    print(True)
else:
    print(False)

if 'good' not in h:                    # this is how we use ' not in ' operator
    print(True)
else:
    print(False)










