                                    # STARTSWITH() AND ENDSWITH()

i = 'hello there!'                                    
print( 'was his first word \' hello \' ? ' + str(i.startswith('hello')) )
print( 'was his last word \' there \' ? ' + str(i.endswith('there')) )
print( 'was his last word \' ! \' ? ' + str(i.endswith('!')) )




























