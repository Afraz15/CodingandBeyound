                                # ISUPPER AND ISLOWER CASE

hi = 'hello World'

print(hi.islower())
print(hi.isupper())

print('hello'.islower())
print('HELLO'.isupper())

print('Hello'.islower())
print('hELLO'.isupper())

print('hello123'.islower())         
print('HI123'.isupper())

print('123'.islower())              # because it does have any letter is neither lower nor upper
