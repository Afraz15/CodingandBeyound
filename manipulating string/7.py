                                # PUTTING STRING INSIDE OTHER STRING

name = 'ali'
age = 19

print('My name is '+ name + ' and i am ' + str(age) + ' years old')     

# this can be said to be the first type or old way to write string...

print('My name is %s and i am %s years old ' %(name,age))

# this is the next level type

print(f'My name is {name} and i am {age} years old')

# this the newest way which is of python 3.6













